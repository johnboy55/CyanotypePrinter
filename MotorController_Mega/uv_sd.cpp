bool loadCalibrationFile(const String &path) {
  File f = SD.open(path);
  if (!f) return false;
  String all;
  while (f.available()) all += (char)f.read();
  f.close();

  int n = 0, start = 0;
  while (n < 16) {
    int comma = all.indexOf(',', start);
    String tok = (comma < 0) ? all.substring(start) : all.substring(start, comma);
    tok.trim();
    if (tok.length() == 0) break;
    LUT16[n++] = tok.toFloat();
    if (comma < 0) break;
    start = comma + 1;
  }
  return (n == 16);
}